# Não é possivel adicionar remover ou ordenar seu elementos
# Para alterar um tupla é necessário refatora-la (rescrever no código)

# O que define uma tupla é a virgula.
# Uma tupla dentro de outra estruutra de dados precisa ser fatorada  com os
# parenteses ()



tupla_numeros = (1,45,12,67)
tupla_carros = "Gol","Fiesta","Sandeiro"
